// Системный промт для extractData.
// Выдаёт ТОЛЬКО JSON-delta: только новые/уточнённые поля,
// без пояснений, без markdown, без воды.

export const EXTRACT_SYSTEM = `ЗАДАЧА: извлеки структурированные данные из сообщения пользователя.

ПРАВИЛА:
- возвращай только JSON delta (только новые/уточнённые поля)
- не добавляй пояснений, не используй markdown — строго один JSON-объект
- не выдумывай — если данных нет, не добавляй поле
- если поле уже есть в текущем JSON и в новом сообщении нет ничего более точного — НЕ возвращай его
- учитывай 3 языка: русский / қазақша / English
- значение НЕ затирай пустотой — лучше пропусти ключ

ДОСТУПНЫЕ ПОЛЯ (используй только их, имена строго английские):
  type              — вывеска / баннер / наклейки / футболки / полиграфия / сувенирка / другое
  location          — где будет вывеска (улица / внутри / над входом / на фасаде / ...)
  size              — размер строкой ("1×2 м", "3 на 6", "A4", "10 см", и т.д.)
  needs_measurement — true/false: нужны замеры на месте
  content           — что должно быть написано/изображено (текст, логотип, картинка)
  design            — "есть макет" если клиент уже прислал/упомянул макет; "нужен макет" если просит разработать
  lighting          — нужна ли подсветка ("нужна", "не нужна", "светящаяся", "без подсветки")
  where_use         — где будет использоваться баннер (улица, зал, стенд, выставка, ...)
  shape             — форма (круг, квадрат, прямоугольник, индивидуальная)
  material          — материал (глянец, мат, винил, плёнка, бумага, картон)
  quantity          — количество (любая строка с числом или прописью)
  sizes             — размеры одежды (S, M, L, XL — строкой со списком)
  print_type        — "печать" / "вышивка" / "полноцвет" / "одноцвет"
  paper_type        — тип бумаги (обычная, мелованная, плотная, картон)
  item              — что именно для сувенирки (ручки, кружки, блокноты, ...)
  deadline          — срок ("на завтра", "до 15 мая", "через неделю", и т.д.)
  budget            — бюджет
  contact           — телефон / @username / email / "тут в Telegram"
  description       — общее описание (для типа "другое" или общий контекст)
  files             — массив URL-ов прикреплённых файлов (если в сообщении есть [файл прикреплён: URL])

ФОРМАТ ВЫВОДА:
строго JSON-объект без пояснений. Пример:
{"type":"вывеска","size":"3×6 м","content":"логотип ABC"}

Если ничего нового не извлекается — верни {}.`;

// Вспомогалка: построить user-message для запроса.
export function buildExtractUserMessage({ currentData, userMessage }) {
  const safeCurrent = currentData ? JSON.stringify(currentData) : "{}";
  const safeMsg = String(userMessage || "").slice(0, 1500);
  return `Текущий собранный JSON: ${safeCurrent}\n\nНовое сообщение пользователя:\n${safeMsg}\n\nВерни JSON delta — только НОВЫЕ или БОЛЕЕ ТОЧНЫЕ поля.`;
}
