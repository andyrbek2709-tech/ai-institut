// Промт для генерации коммерческого предложения (КП) клиенту.
// На входе: orderData (собранные данные заявки), history (диалог),
// knowledgeContext (RAG из knowledge_base), lang (ru/kk/en).
// На выходе: готовый текст КП ~150-250 слов, простой текст без markdown.

const LANG_INSTRUCTION = {
  ru: "Отвечай на русском языке.",
  kk: "Қазақ тілінде жауап бер.",
  en: "Reply in English.",
};

export const PROPOSAL_SYSTEM = `Ты — менеджер рекламного агентства, который пишет короткое коммерческое предложение (КП) клиенту по его заявке.

Структура КП:
1) Тёплое приветствие + 1–2 фразы: что мы поняли из заявки.
2) Что входит в работу — буллеты (каждый с новой строки, маркер "—" или "•"). Если в данных есть extras / доп. услуги — обязательно перечисли их.
3) Срок: если deadline есть в данных — конкретно; если нет — "срок согласуем после подтверждения".
4) Стоимость: если есть в базе знаний или в orderData — укажи как ориентир; если нет — "стоимость уточним после подтверждения деталей" (не выдумывай цифры).
5) Завершение с мягким CTA: подтвердить заказ или обсудить детали.

Стиль:
- Дружелюбно, по-человечески, без канцелярита.
- 150–250 слов. Лаконично.
- ПРОСТОЙ ТЕКСТ. Без markdown (**, ##, _), без эмодзи (можно один в конце, если уместно).
- Между блоками — пустая строка, читаемо.
- Без подписи и без префикса "Менеджер:" / "КП:" — это уже добавит интерфейс.
- Не обещай скидок и не выдумывай данные, которых нет.

Используй базу знаний (если есть) как ориентир для материалов и цен, но переформулируй в контексте заказа.

Верни ТОЛЬКО текст КП — без кавычек, без пояснений.`;

export function buildProposalUserMessage({
  orderData = {},
  history = [],
  lang = "ru",
  knowledgeContext = "",
} = {}) {
  const langInstr = LANG_INSTRUCTION[lang] || LANG_INSTRUCTION.ru;

  // Только непустые поля orderData — для компактного промта.
  const compact = {};
  for (const [k, v] of Object.entries(orderData || {})) {
    if (v == null) continue;
    if (typeof v === "string" && !v.trim()) continue;
    if (Array.isArray(v) && v.length === 0) continue;
    compact[k] = v;
  }

  // Последние 6 сообщений диалога — контекст того, что обсуждали.
  const recent = (history || [])
    .slice(-6)
    .map((m) => {
      const role =
        m.role === "user" ? "Клиент" :
        m.role === "manager" ? "Менеджер" :
        "Бот";
      const content = typeof m.content === "string" ? m.content : JSON.stringify(m.content);
      return `${role}: ${content}`;
    })
    .join("\n");

  const parts = [
    langInstr,
    "",
    `Данные заявки (только заполненные поля):\n${JSON.stringify(compact, null, 2)}`,
    "",
  ];

  if (knowledgeContext && String(knowledgeContext).trim()) {
    parts.push(`БАЗА ЗНАНИЙ (используй как ориентир, не цитируй дословно):\n${knowledgeContext}`);
    parts.push("");
  }

  parts.push(`Контекст диалога (последние сообщения):\n${recent || "(диалог только начался)"}`);
  parts.push("");
  parts.push(
    "Сформулируй коммерческое предложение по структуре из system-промта, на языке клиента. " +
    "Объём 150–250 слов. Только текст КП, без кавычек."
  );

  return parts.join("\n");
}
