/**
 * Build system prompt for given lang code: "ru" | "kk" | "en".
 */
export function buildSystemPrompt(lang = "ru") {
  const meta = LANG_META[lang] || LANG_META.ru;
  return SYSTEM_PROMPT_BASE.replace("{LANG_NAME}", meta.name);
}

